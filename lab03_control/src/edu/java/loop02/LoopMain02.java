package edu.java.loop02;

public class LoopMain02 {

    public static void main(String[] args) {
        // 복합 할당 연산자: +=, -=, *=, /=, ...
        
        // 0 2 4 6 8 10을 한 줄씩 출력.
        
        for(int n = 0 ; n <= 10 ; n+=2)
            System.out.println(n);
        
    }

}
